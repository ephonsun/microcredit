package banger.framework.component.dataimport;

/**
 * Excel导入结果
 * @author zhusw
 *
 */
public interface IImportResult {
	
	/**
	 * 成功多少条
	 * @return
	 */
	int getSuccessCount();

	/**
	 * 失败多少条
	 * @return
	 */
	int getFailCount();
	
	/**
	 * 共总多少行
	 * @return
	 */
	int getRowCount();
	
	/**
	 * 新增多少条
	 * @return
	 */
	int getInsertCount();
	
	/**
	 * 修改多少条
	 * @return
	 */
	int getUpdateCount();
	
	/**
	 * 导入失败的记录文件
	 * @return
	 */
	String getFailFilename();

    /**
     * 导入的所有记录文件
     * @return
     */
    String getReportFileName();
	
}
