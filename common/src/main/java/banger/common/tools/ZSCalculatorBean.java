package banger.common.tools;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;

/**
 * Created by kangbiao on 2017/11
 */

public class ZSCalculatorBean implements Serializable {

    //当前期数
    private int sortNo;
    //月供金额
    private BigDecimal numMonthPay;
    //月供本金
    private BigDecimal numMonthCapital;
    //月供利息
    private BigDecimal numMonthInterest;
    //剩余本金
    private BigDecimal numLeftCapital;
    //计息天数
    private int numDays;
    //应还款日期
    private Date repayDate;

    public int getSortNo() {
        return sortNo;
    }

    public void setSortNo(int sortNo) {
        this.sortNo = sortNo;
    }

    public BigDecimal getNumMonthPay() {
        return numMonthPay;
    }

    public void setNumMonthPay(BigDecimal numMonthPay) {
        this.numMonthPay = numMonthPay;
    }

    public BigDecimal getNumMonthCapital() {
        return numMonthCapital;
    }

    public void setNumMonthCapital(BigDecimal numMonthCapital) {
        this.numMonthCapital = numMonthCapital;
    }

    public BigDecimal getNumMonthInterest() {
        return numMonthInterest;
    }

    public void setNumMonthInterest(BigDecimal numMonthInterest) {
        this.numMonthInterest = numMonthInterest;
    }

    public BigDecimal getNumLeftCapital() {
        return numLeftCapital;
    }

    public void setNumLeftCapital(BigDecimal numLeftCapital) {
        this.numLeftCapital = numLeftCapital;
    }

    public int getNumDays() {
        return numDays;
    }

    public void setNumDays(int numDays) {
        this.numDays = numDays;
    }

    public Date getRepayDate() {
        return repayDate;
    }

    public void setRepayDate(Date repayDate) {
        this.repayDate = repayDate;
    }
}
