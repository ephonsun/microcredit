package banger.domain.config;

import java.io.Serializable;

import banger.framework.entity.Entity;

/**
 * 自动导入配置信息表
 */
public class AutoImportSetting implements Serializable {
	private static final long serialVersionUID = 7261742811060104821L;
	private Integer settingId;					//配置ID
	private String importCode;					//配置编码
	private String importName;					//数据导入配置名称
	private String importDisplay;					//数据导入配置描述
	private String dataSourceType;					//数据来源类型DB_TABLE    数据库表DATA_FILE   文件
	private String tableName;					//数据库表名
	private Integer isActived;					//是否启用0 禁用1 启用
	private String dataFilePath;					//数据文件路径
	private String dataFileName;					//数据文件名
	private Integer matchMode;                    //匹配方式

	public Integer getSettingId(){
		return this.settingId;
	}

	public void setSettingId(Integer settingId){
		this.settingId=settingId;
	}

	public String getImportCode(){
		return this.importCode;
	}

	public void setImportCode(String importCode){
		this.importCode=importCode;
	}

	public String getImportName(){
		return this.importName;
	}

	public void setImportName(String importName){
		this.importName=importName;
	}

	public String getImportDisplay(){
		return this.importDisplay;
	}

	public void setImportDisplay(String importDisplay){
		this.importDisplay=importDisplay;
	}

	public String getDataSourceType(){
		return this.dataSourceType;
	}

	public void setDataSourceType(String dataSourceType){
		this.dataSourceType=dataSourceType;
	}

	public String getTableName(){
		return this.tableName;
	}

	public void setTableName(String tableName){
		this.tableName=tableName;
	}

	public Integer getIsActived(){
		return this.isActived;
	}

	public void setIsActived(Integer isActived){
		this.isActived=isActived;
	}

	public String getDataFilePath(){
		return this.dataFilePath;
	}

	public void setDataFilePath(String dataFilePath){
		this.dataFilePath=dataFilePath;
	}

	public String getDataFileName(){
		return this.dataFileName;
	}

	public void setDataFileName(String dataFileName){
		this.dataFileName=dataFileName;
	}

	public Integer getMatchMode() {
		return matchMode;
	}

	public void setMatchMode(Integer matchMode) {
		this.matchMode = matchMode;
	}
}
