public class IntoAutoTableColumnQuery extends IntoAutoTableColumn{
	private String tableModelName;
	private String fieldName;
	private String fieldType;
	private String fieldColumnDisplay;

	public String getFieldColumnDisplay() {
		return fieldColumnDisplay;
	}

	public void setFieldColumnDisplay(String fieldColumnDisplay) {
		this.fieldColumnDisplay = fieldColumnDisplay;
	}

	public String getTableModelName() {
		return tableModelName;
	}

	public void setTableModelName(String tableModelName) {
		this.tableModelName = tableModelName;
	}

	public String getFieldName() {
		return fieldName;
	}

	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}

	public String getFieldType() {
		return fieldType;
	}

	public void setFieldType(String fieldType) {
		this.fieldType = fieldType;
	}
}
