package banger.domain.config;

import java.io.Serializable;

import banger.framework.entity.Entity;

/**
 * 自动导入配置项表
 */
public class AutoImportSettingItem implements Serializable {
	private static final long serialVersionUID = 5519518387028514703L;
	private Integer itemId;					//配置项ID
	private String importCode;					//配置编码
	private Integer settingId;					//配置ID
	private String sourceTableColumn;					//来源数据表列名
	private Integer sourceColumnIndex;					//来源数据列序号
	private String sourceColumnDisplay;                  //来源数据表列名描述
	private String targetTableColumn;					//目标表列名
	private String targetTableName;					//目标表表名
	private String targetColumnType;					//目标表列字段类型
	private String targetColumnDictName;					//目标表列代码表
	private Integer isCovered;					//是否复盖1 复盖0 不复盖
	private Integer isRequired;					//是否必填1 必填0 不必填
	private Integer targetColumnId;              //自定义字段列ID
	private String targetColumnDisplay;          //数据库自定义字段名称

	public Integer getTargetColumnId() {
		return targetColumnId;
	}

	public void setTargetColumnId(Integer targetColumnId) {
		this.targetColumnId = targetColumnId;
	}

	public String getTargetColumnDisplay() {
		return targetColumnDisplay;
	}

	public void setTargetColumnDisplay(String targetColumnDisplay) {
		this.targetColumnDisplay = targetColumnDisplay;
	}

	public Integer getItemId(){
		return this.itemId;
	}

	public void setItemId(Integer itemId){
		this.itemId=itemId;
	}

	public String getImportCode(){
		return this.importCode;
	}

	public void setImportCode(String importCode){
		this.importCode=importCode;
	}

	public Integer getSettingId(){
		return this.settingId;
	}

	public void setSettingId(Integer settingId){
		this.settingId=settingId;
	}

	public String getSourceTableColumn(){
		return this.sourceTableColumn;
	}

	public void setSourceTableColumn(String sourceTableColumn){
		this.sourceTableColumn=sourceTableColumn;
	}

	public Integer getSourceColumnIndex(){
		return this.sourceColumnIndex;
	}

	public void setSourceColumnIndex(Integer sourceColumnIndex){
		this.sourceColumnIndex=sourceColumnIndex;
	}

	public String getTargetTableColumn(){
		return this.targetTableColumn;
	}

	public void setTargetTableColumn(String targetTableColumn){
		this.targetTableColumn=targetTableColumn;
	}

	public String getTargetTableName(){
		return this.targetTableName;
	}

	public void setTargetTableName(String targetTableName){
		this.targetTableName=targetTableName;
	}

	public String getTargetColumnType(){
		return this.targetColumnType;
	}

	public void setTargetColumnType(String targetColumnType){
		this.targetColumnType=targetColumnType;
	}

	public String getTargetColumnDictName(){
		return this.targetColumnDictName;
	}

	public void setTargetColumnDictName(String targetColumnDictName){
		this.targetColumnDictName=targetColumnDictName;
	}

	public Integer getIsCovered(){
		return this.isCovered;
	}

	public void setIsCovered(Integer isCovered){
		this.isCovered=isCovered;
	}

	public Integer getIsRequired(){
		return this.isRequired;
	}

	public void setIsRequired(Integer isRequired){
		this.isRequired=isRequired;
	}

	public String getSourceColumnDisplay() {
		return sourceColumnDisplay;
	}

	public void setSourceColumnDisplay(String sourceColumnDisplay) {
		this.sourceColumnDisplay = sourceColumnDisplay;
	}
}
