package banger.domain.config;

import java.io.Serializable;
import java.util.Date;

import banger.framework.entity.Entity;

/**
 * 初审规则表
 */
public class IntoTrialRuleInfo implements Serializable {
	private static final long serialVersionUID = 2431757347527739312L;
	private Integer ruleId;					//主键
	private String ruleName;					//规则名称
	private Date createDate;					//创建时间
	private Date updateDate;					//更新时间
	private Integer createUser;					//创建用户
	private Integer updateUser;					//更新用户

	public Integer getRuleId(){
		return this.ruleId;
	}

	public void setRuleId(Integer ruleId){
		this.ruleId=ruleId;
	}

	public String getRuleName(){
		return this.ruleName;
	}

	public void setRuleName(String ruleName){
		this.ruleName=ruleName;
	}

	public Date getCreateDate(){
		return this.createDate;
	}

	public void setCreateDate(Date createDate){
		this.createDate=createDate;
	}

	public Date getUpdateDate(){
		return this.updateDate;
	}

	public void setUpdateDate(Date updateDate){
		this.updateDate=updateDate;
	}

	public Integer getCreateUser(){
		return this.createUser;
	}

	public void setCreateUser(Integer createUser){
		this.createUser=createUser;
	}

	public Integer getUpdateUser(){
		return this.updateUser;
	}

	public void setUpdateUser(Integer updateUser){
		this.updateUser=updateUser;
	}

}
