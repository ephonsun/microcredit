package banger.domain.loan.tmp;

import java.io.Serializable;
import java.math.BigDecimal;

import banger.framework.entity.Entity;

/**
 * 还款记录
 */
public class TmpLoanRepaymentItem implements Serializable {
	private static final long serialVersionUID = 2736423527212322296L;
	private String loanAccount;					//贷款帐号
	private String repaymentType;					//还款种类
	private String repaymentDate;					//还款日期
	private String repaymentTime;					//还款时间
	private BigDecimal repaymentAmount;					//还款金额

	public String getLoanAccount(){
		return this.loanAccount;
	}

	public void setLoanAccount(String loanAccount){
		this.loanAccount=loanAccount;
	}

	public String getRepaymentType(){
		return this.repaymentType;
	}

	public void setRepaymentType(String repaymentType){
		this.repaymentType=repaymentType;
	}

	public String getRepaymentDate(){
		return this.repaymentDate;
	}

	public void setRepaymentDate(String repaymentDate){
		this.repaymentDate=repaymentDate;
	}

	public String getRepaymentTime(){
		return this.repaymentTime;
	}

	public void setRepaymentTime(String repaymentTime){
		this.repaymentTime=repaymentTime;
	}

	public BigDecimal getRepaymentAmount(){
		return this.repaymentAmount;
	}

	public void setRepaymentAmount(BigDecimal repaymentAmount){
		this.repaymentAmount=repaymentAmount;
	}

}
