package banger.domain.loan.tmp;

import java.io.Serializable;
import java.math.BigDecimal;

import banger.framework.entity.Entity;

/**
 * 贷款账户信息接口
 */
public class TmpLoanAccount implements Serializable {
	private static final long serialVersionUID = 3193884928716561186L;
	private String loanAccount;					//贷款帐号
	private String iouNo;					//借据帐号
	private String contractNo;					//贷款合同号
	private String customerNo;					//客户号
	private BigDecimal loanBalance;					//贷款余额
	private String repaymentAccount;					//还款帐号
	private BigDecimal repaymentBalance;					//还款账户余额
	private String loanStatus;					//贷款状态
	private BigDecimal nextRepaymentAmount;					//下一次还款金额

	public String getLoanAccount(){
		return this.loanAccount;
	}

	public void setLoanAccount(String loanAccount){
		this.loanAccount=loanAccount;
	}

	public String getIouNo(){
		return this.iouNo;
	}

	public void setIouNo(String iouNo){
		this.iouNo=iouNo;
	}

	public String getContractNo(){
		return this.contractNo;
	}

	public void setContractNo(String contractNo){
		this.contractNo=contractNo;
	}

	public String getCustomerNo(){
		return this.customerNo;
	}

	public void setCustomerNo(String customerNo){
		this.customerNo=customerNo;
	}

	public BigDecimal getLoanBalance(){
		return this.loanBalance;
	}

	public void setLoanBalance(BigDecimal loanBalance){
		this.loanBalance=loanBalance;
	}

	public String getRepaymentAccount(){
		return this.repaymentAccount;
	}

	public void setRepaymentAccount(String repaymentAccount){
		this.repaymentAccount=repaymentAccount;
	}

	public BigDecimal getRepaymentBalance(){
		return this.repaymentBalance;
	}

	public void setRepaymentBalance(BigDecimal repaymentBalance){
		this.repaymentBalance=repaymentBalance;
	}

	public String getLoanStatus(){
		return this.loanStatus;
	}

	public void setLoanStatus(String loanStatus){
		this.loanStatus=loanStatus;
	}

	public BigDecimal getNextRepaymentAmount(){
		return this.nextRepaymentAmount;
	}

	public void setNextRepaymentAmount(BigDecimal nextRepaymentAmount){
		this.nextRepaymentAmount=nextRepaymentAmount;
	}

}
