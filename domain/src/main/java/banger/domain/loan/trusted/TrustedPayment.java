/**
 * 受托支付
 */
public class TrustedPayment extends LoanTrustedPayment {


    private String loanUse;//用途
    private String loanName;//贷款用户名
    private String loanIdentifyNum;//证件号码
    private String loanTelNum;//电话


    public String getLoanUse() {
        return loanUse;
    }

    public void setLoanUse(String loanUse) {
        this.loanUse = loanUse;
    }

    public String getLoanName() {
        return loanName;
    }

    public void setLoanName(String loanName) {
        this.loanName = loanName;
    }

    public String getLoanIdentifyNum() {
        return loanIdentifyNum;
    }

    public void setLoanIdentifyNum(String loanIdentifyNum) {
        this.loanIdentifyNum = loanIdentifyNum;
    }

    public String getLoanTelNum() {
        return loanTelNum;
    }

    public void setLoanTelNum(String loanTelNum) {
        this.loanTelNum = loanTelNum;
    }
}
