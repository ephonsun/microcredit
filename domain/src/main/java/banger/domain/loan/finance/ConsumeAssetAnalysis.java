package banger.domain.loan.finance;

import java.math.BigDecimal;

/**
 * 消费类资产分析
 * Created by Administrator on 2017/8/21.
 */
public class ConsumeAssetAnalysis {

    //    总资产	总资产=各项资产总和（不包含表外资产）
    private BigDecimal total;
    //    现金		来源于资产负债表填入的信息
    private BigDecimal money;
    //    投资性资产
    private BigDecimal investmentAssets;
    //    对外债权
    private BigDecimal externalClaims;
    //    预付款
    private BigDecimal advanceCharge;
    //    固定资产
    private BigDecimal fixedAssets;
    //    其他资产
    private BigDecimal otherAssets;
    //    总负债	总负债=各项负债总和（不包含表外资产）
    private BigDecimal liabilities;
    //    消费性负债		来源于资产负债表填入的信息
    private BigDecimal consumerLiabilities;
    //    自用性负债
    private BigDecimal personalLiabilities;
    //    投资性负债
    private BigDecimal investmentLiability;
    //    其他负债
    private BigDecimal otherLiability;
    //    实际权益	实际权益=总资产-总负债
    private BigDecimal actualInterest;
    //    资产负债率	资产负债率=总负债/总资产*100%（保留1位小数）比较行业指引数据
    private BigDecimal assetLiabilityRatio;

    //    资产负债率 比较行业指引数据结果说明
    private String assetLiabilityRatioExp;

    //    资产负债率 比较行业指引数据结果等级
    private String assetLiabilityRatioGrade;
    //    资产负债率 比较行业指引数据结果等级英文
    private String assetLiabilityRatioGradeEn;

    public String getAssetLiabilityRatioGradeEn() {
        return assetLiabilityRatioGradeEn;
    }

    public void setAssetLiabilityRatioGradeEn(String assetLiabilityRatioGradeEn) {
        this.assetLiabilityRatioGradeEn = assetLiabilityRatioGradeEn;
    }

    public void setAssetLiabilityRatioExp(String assetLiabilityRatioExp) {
        this.assetLiabilityRatioExp = assetLiabilityRatioExp;
    }

    public String getAssetLiabilityRatioGrade() {
        return assetLiabilityRatioGrade;
    }

    public void setAssetLiabilityRatioGrade(String assetLiabilityRatioGrade) {
        this.assetLiabilityRatioGrade = assetLiabilityRatioGrade;
    }

    public String getAssetLiabilityRatioExp() {
        return assetLiabilityRatioExp;
    }

    public BigDecimal getTotal() {
        return total;
    }

    public void setTotal(BigDecimal total) {
        this.total = total;
    }

    public BigDecimal getMoney() {
        return money;
    }

    public void setMoney(BigDecimal money) {
        this.money = money;
    }

    public BigDecimal getInvestmentAssets() {
        return investmentAssets;
    }

    public void setInvestmentAssets(BigDecimal investmentAssets) {
        this.investmentAssets = investmentAssets;
    }

    public BigDecimal getExternalClaims() {
        return externalClaims;
    }

    public void setExternalClaims(BigDecimal externalClaims) {
        this.externalClaims = externalClaims;
    }

    public BigDecimal getAdvanceCharge() {
        return advanceCharge;
    }

    public void setAdvanceCharge(BigDecimal advanceCharge) {
        this.advanceCharge = advanceCharge;
    }

    public BigDecimal getFixedAssets() {
        return fixedAssets;
    }

    public void setFixedAssets(BigDecimal fixedAssets) {
        this.fixedAssets = fixedAssets;
    }

    public BigDecimal getOtherAssets() {
        return otherAssets;
    }

    public void setOtherAssets(BigDecimal otherAssets) {
        this.otherAssets = otherAssets;
    }

    public BigDecimal getLiabilities() {
        return liabilities;
    }

    public void setLiabilities(BigDecimal liabilities) {
        this.liabilities = liabilities;
    }

    public BigDecimal getConsumerLiabilities() {
        return consumerLiabilities;
    }

    public void setConsumerLiabilities(BigDecimal consumerLiabilities) {
        this.consumerLiabilities = consumerLiabilities;
    }

    public BigDecimal getPersonalLiabilities() {
        return personalLiabilities;
    }

    public void setPersonalLiabilities(BigDecimal personalLiabilities) {
        this.personalLiabilities = personalLiabilities;
    }

    public BigDecimal getInvestmentLiability() {
        return investmentLiability;
    }

    public void setInvestmentLiability(BigDecimal investmentLiability) {
        this.investmentLiability = investmentLiability;
    }

    public BigDecimal getOtherLiability() {
        return otherLiability;
    }

    public void setOtherLiability(BigDecimal otherLiability) {
        this.otherLiability = otherLiability;
    }

    public BigDecimal getActualInterest() {
        return actualInterest;
    }

    public void setActualInterest(BigDecimal actualInterest) {
        this.actualInterest = actualInterest;
    }

    public BigDecimal getAssetLiabilityRatio() {
        return assetLiabilityRatio;
    }

    public void setAssetLiabilityRatio(BigDecimal assetLiabilityRatio) {
        this.assetLiabilityRatio = assetLiabilityRatio;
    }
}
