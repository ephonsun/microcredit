package banger.domain.loan.finance;

import java.math.BigDecimal;

/**
 * 经营类偿还能力分析
 * Created by Administrator on 2017/8/21.
 */
public class ManageSolvencyAnalysis {

    //现金比	现金比=现金/总负债*100%（四舍五入，保留1位小数）
    private BigDecimal cashRatio;
    //    流动比	流动比=流动资产/流动负债*100%（四舍五入，保留1位小数）
    private BigDecimal flowRatio;
    //    销货比	销货比=（短期负债+长期负债）/营业收入*100%（四舍五入，保留1位小数）
    private BigDecimal salesRatio;
    //    速动比	速动比=（流动资产-存货）/流动负债*100%（四舍五入，保留1位小数）	比较行业指引数据
    private BigDecimal quickRatio;
    //    速动比 比较行业指引数据说明
    private String quickRatioExp;
    //    速动比 比较行业指引数据等级
    private String quickRatioGrade;
    //    速动比 比较行业指引数据等级
    private String quickRatioGradeEn;

    public String getQuickRatioGradeEn() {
        return quickRatioGradeEn;
    }

    public void setQuickRatioGradeEn(String quickRatioGradeEn) {
        this.quickRatioGradeEn = quickRatioGradeEn;
    }

    public String getQuickRatioExp() {
        return quickRatioExp;
    }

    public void setQuickRatioExp(String quickRatioExp) {
        this.quickRatioExp = quickRatioExp;
    }

    public String getQuickRatioGrade() {
        return quickRatioGrade;
    }

    public void setQuickRatioGrade(String quickRatioGrade) {
        this.quickRatioGrade = quickRatioGrade;
    }

    public BigDecimal getCashRatio() {
        return cashRatio;
    }

    public void setCashRatio(BigDecimal cashRatio) {
        this.cashRatio = cashRatio;
    }

    public BigDecimal getFlowRatio() {
        return flowRatio;
    }

    public void setFlowRatio(BigDecimal flowRatio) {
        this.flowRatio = flowRatio;
    }

    public BigDecimal getSalesRatio() {
        return salesRatio;
    }

    public void setSalesRatio(BigDecimal salesRatio) {
        this.salesRatio = salesRatio;
    }

    public BigDecimal getQuickRatio() {
        return quickRatio;
    }

    public void setQuickRatio(BigDecimal quickRatio) {
        this.quickRatio = quickRatio;
    }
}
