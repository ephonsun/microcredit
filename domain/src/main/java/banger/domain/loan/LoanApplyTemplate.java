package banger.domain.loan;

import banger.domain.config.AutoBaseTemplate;

/**
 * 得到贷款模板
 * @author zhusw
 *
 */
public class LoanApplyTemplate extends AutoBaseTemplate {
	protected Integer fieldCount;					   //字段数
	protected Integer inputFieldCount;				  //已输入的字段数
	protected Integer requireFieldCount;			 //必填字段数
	protected Integer inputRequireFieldCount;		//已输入的必填字段数
	
	public LoanApplyTemplate(){
		
	}
	
	public LoanApplyTemplate(Integer id,String name,String type){
		this.setId(id);
		this.setName(name);
		this.setType(type);
	}

	public Integer getFieldCount() {
		return fieldCount;
	}

	public void setFieldCount(Integer fieldCount) {
		this.fieldCount = fieldCount;
	}

	public Integer getInputFieldCount() {
		return inputFieldCount;
	}

	public void setInputFieldCount(Integer inputFieldCount) {
		this.inputFieldCount = inputFieldCount;
	}

	public Integer getRequireFieldCount() {
		return requireFieldCount;
	}

	public void setRequireFieldCount(Integer requireFieldCount) {
		this.requireFieldCount = requireFieldCount;
	}

	public Integer getInputRequireFieldCount() {
		return inputRequireFieldCount;
	}

	public void setInputRequireFieldCount(Integer inputRequireFieldCount) {
		this.inputRequireFieldCount = inputRequireFieldCount;
	}
	
}
