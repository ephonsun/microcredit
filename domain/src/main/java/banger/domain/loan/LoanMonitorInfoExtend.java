package banger.domain.loan;


import banger.framework.codetable.CodeTable;

public class LoanMonitorInfoExtend extends LoanMonitorInfo {

    @CodeTable(name = "cdDictColByName", key = "reType", params = "CD_MONITOR_REVISIT_TYPE")
    private String reType;					//回访类型
    private String dictName;
    private String userName;
    private Boolean isManager;

    public String getDictName() {
        return dictName;
    }

    public void setDictName(String dictName) {
        this.dictName = dictName;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getReType() {
        return reType;
    }

    public void setReType(String reType) {
        this.reType = reType;
    }

    public Boolean getManager() {
        return isManager;
    }

    public void setManager(Boolean manager) {
        isManager = manager;
    }
}
