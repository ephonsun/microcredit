package banger.domain.permission;

import java.io.Serializable;

/**
 * 用户信息
 */
public class PmsUserInfo implements Serializable {
	private static final long serialVersionUID = 3941001833997735456L;

	private Integer userId;
	private Integer userDeptId;
	private Integer voipPortId;
	private String userAccount;
	private String userPassword;
	private String userCode;
	private String userName;
	private String roleIds;
	private String inchargeDeptIds;
	private String deptName;
	private String sessionId;
	private String ip;
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getUserDeptId() {
		return userDeptId;
	}
	public void setUserDeptId(Integer userDeptId) {
		this.userDeptId = userDeptId;
	}
	public String getUserAccount() {
		return userAccount;
	}
	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}
	public String getUserCode() {
		return userCode;
	}
	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getRoleIds() {
		return roleIds;
	}
	public void setRoleIds(String roleIds) {
		this.roleIds = roleIds;
	}
	public String getDeptName() {
		return deptName;
	}
	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}
	public String getSessionId() {
		return sessionId;
	}
	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}
	
	public String getInchargeDeptIds() {
		return inchargeDeptIds;
	}
	public void setInchargeDeptIds(String inchargeDeptIds) {
		this.inchargeDeptIds = inchargeDeptIds;
	}
	public Integer getVoipPortId() {
		return voipPortId;
	}
	public void setVoipPortId(Integer voipPortId) {
		this.voipPortId = voipPortId;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getUserPassword() {
		return userPassword;
	}

	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
}
