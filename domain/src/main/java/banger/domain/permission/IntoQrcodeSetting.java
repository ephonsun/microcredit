package banger.domain.permission;

import java.io.Serializable;

/**
 * 进件二维码配置表
 */
public class IntoQrcodeSetting implements Serializable {
	private static final long serialVersionUID = 2159866937938054064L;
	private Integer qrId;					//主键
	private String accessPath;					//访问路径
	private String imaPath;					//图片路径
	private String imaName;					//图片名称
	private Integer imaSize;					//图片大小

	public Integer getQrId(){
		return this.qrId;
	}

	public void setQrId(Integer qrId){
		this.qrId=qrId;
	}

	public String getAccessPath(){
		return this.accessPath;
	}

	public void setAccessPath(String accessPath){
		this.accessPath=accessPath;
	}

	public String getImaPath(){
		return this.imaPath;
	}

	public void setImaPath(String imaPath){
		this.imaPath=imaPath;
	}

	public String getImaName(){
		return this.imaName;
	}

	public void setImaName(String imaName){
		this.imaName=imaName;
	}

	public Integer getImaSize(){
		return this.imaSize;
	}

	public void setImaSize(Integer imaSize){
		this.imaSize=imaSize;
	}

}
