package banger.domain.task;


/**
 * 团队成员任务分配明细表
 */
public class TskTeamMemberTaskAssignQuery extends TskTeamMemberTaskAssign{
	private String teamMemberName;
	
	private Integer assignTarget;//任务目标
	
	private Integer unSiginedNum;//未分配
	
	private String assignPercent;//已分配比率
	
	private String finishPercent;//完成百分比

	private String finishNumF;

	private String assignTargetF;

	private String assignNumF;

	private String unSiginedNumF;//未分配数量

	public String getTeamMemberName() {
		return teamMemberName;
	}

	public void setTeamMemberName(String teamMemberName) {
		this.teamMemberName = teamMemberName;
	}

	public String getAssignPercent() {
		return assignPercent;
	}

	public void setAssignPercent(String assignPercent) {
		this.assignPercent = assignPercent;
	}

	public String getFinishPercent() {
		return finishPercent;
	}

	public void setFinishPercent(String finishPercent) {
		this.finishPercent = finishPercent;
	}

	public Integer getAssignTarget() {
		return assignTarget;
	}

	public void setAssignTarget(Integer assignTarget) {
		this.assignTarget = assignTarget;
	}

	public Integer getUnSiginedNum() {
		return unSiginedNum;
	}

	public void setUnSiginedNum(Integer unSiginedNum) {
		this.unSiginedNum = unSiginedNum;
	}

	public String getAssignTargetF() {
		return assignTargetF;
	}

	public void setAssignTargetF(String assignTargetF) {
		this.assignTargetF = assignTargetF;
	}

	public String getAssignNumF() {
		return assignNumF;
	}

	public void setAssignNumF(String assignNumF) {
		this.assignNumF = assignNumF;
	}

	public String getUnSiginedNumF() {
		return unSiginedNumF;
	}

	public void setUnSiginedNumF(String unSiginedNumF) {
		this.unSiginedNumF = unSiginedNumF;
	}

	public String getFinishNumF() {
		return finishNumF;
	}

	public void setFinishNumF(String finishNumF) {
		this.finishNumF = finishNumF;
	}
}
