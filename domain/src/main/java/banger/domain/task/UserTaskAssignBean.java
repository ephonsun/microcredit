package banger.domain.task;

/**
 * 客户经理任务分配
 * 
 */
public class UserTaskAssignBean {

	private String userName;
	private Integer userId;
	private Integer assignNum;//分配目标数量
	private Integer teamMemberTaskAssignId;
	

	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getAssignNum() {
		return assignNum;
	}
	public void setAssignNum(Integer assignNum) {
		this.assignNum = assignNum;
	}
	public Integer getTeamMemberTaskAssignId() {
		return teamMemberTaskAssignId;
	}
	public void setTeamMemberTaskAssignId(Integer teamMemberTaskAssignId) {
		this.teamMemberTaskAssignId = teamMemberTaskAssignId;
	}
		
}
