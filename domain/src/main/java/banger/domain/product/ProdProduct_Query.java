package banger.domain.product;

import banger.framework.codetable.CodeTable;

public class ProdProduct_Query extends ProdProduct{

	private static final long serialVersionUID = -3761025198409434369L;
	
	@CodeTable(name="cdDictColByName",key="productType",params="CD_PRODUCT_TYPE")
	private String productTypeName ;//产品类型名

	private String loanTypeName; //贷款类型名

	public String getProductTypeName() {
		return productTypeName;
	}
	
	public void setProductTypeName(String productTypeName) {
		this.productTypeName = productTypeName;
	}

	public String getLoanTypeName() {
		return loanTypeName;
	}

	public void setLoanTypeName(String loanTypeName) {
		this.loanTypeName = loanTypeName;
	}
}
