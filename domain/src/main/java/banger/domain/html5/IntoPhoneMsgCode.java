package banger.domain.html5;

import java.io.Serializable;
import java.util.Date;

import banger.framework.entity.Entity;

/**
 * 短信验证码表
 */
public class IntoPhoneMsgCode implements Serializable {
	private static final long serialVersionUID = 1058904603226754718L;
	private Integer id;					//主键
	private String phone;					//手机号码
	private String checkCode;					//验证码
	private Date addTime;					//生成时间
	private Integer isUse;					//是否使用
	private String ip;					//IP地址

	public Integer getId(){
		return this.id;
	}

	public void setId(Integer id){
		this.id=id;
	}

	public String getPhone(){
		return this.phone;
	}

	public void setPhone(String phone){
		this.phone=phone;
	}

	public String getCheckCode(){
		return this.checkCode;
	}

	public void setCheckCode(String checkCode){
		this.checkCode=checkCode;
	}

	public Date getAddTime(){
		return this.addTime;
	}

	public void setAddTime(Date addTime){
		this.addTime=addTime;
	}

	public Integer getIsUse(){
		return this.isUse;
	}

	public void setIsUse(Integer isUse){
		this.isUse=isUse;
	}

	public String getIp(){
		return this.ip;
	}

	public void setIp(String ip){
		this.ip=ip;
	}

}
