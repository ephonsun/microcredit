package banger.domain.sub;

/**
 * crm产品&条线
 */
public class CrmPrdType {
    private String ywtx;//业务条线
    private String ywtxbm;//业务条线编码
    private String prdType;//分类
    private String prdName;//产品名称
    private String crmCode;//crm产品编码
    private String subCode;//信贷产品编码

    public String getYwtx() {
        return ywtx;
    }

    public void setYwtx(String ywtx) {
        this.ywtx = ywtx;
    }

    public String getYwtxbm() {
        return ywtxbm;
    }

    public void setYwtxbm(String ywtxbm) {
        this.ywtxbm = ywtxbm;
    }

    public String getPrdType() {
        return prdType;
    }

    public void setPrdType(String prdType) {
        this.prdType = prdType;
    }

    public String getPrdName() {
        return prdName;
    }

    public void setPrdName(String prdName) {
        this.prdName = prdName;
    }

    public String getCrmCode() {
        return crmCode;
    }

    public void setCrmCode(String crmCode) {
        this.crmCode = crmCode;
    }

    public String getSubCode() {
        return subCode;
    }

    public void setSubCode(String subCode) {
        this.subCode = subCode;
    }
}
