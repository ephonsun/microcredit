package banger.domain.layout;

import java.util.Date;

public class BasePage {
	private String id;					//页面唯一标识
	private String title;				//页面标题
	private String author;				//作者
	private Date updateDate;			//修改时间
	private String view;				//页面模板
	private String extraView;			//扩展页面
	private String url;					//页面请求地址
	
	public BasePage(){
		this.id = "";
		this.title = "";
		this.author = "";
		this.view = "";
		this.url = "";
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public Date getUpdateDate() {
		return updateDate;
	}
	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}
	public String getView() {
		return view;
	}
	public void setView(String view) {
		this.view = view;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getExtraView() {
		return extraView;
	}
	public void setExtraView(String extraView) {
		this.extraView = extraView;
	}
	
}
