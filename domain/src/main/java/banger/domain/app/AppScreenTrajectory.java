package banger.domain.app;

import java.io.Serializable;

public class AppScreenTrajectory  implements Serializable {

    private static final long serialVersionUID = 5004287646943648289L;
    private String userName;
    private Double traLongitude;					//经度
    private Double traLatitude;					//纬度
    private String deviceId;					//设备ID

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public static long getSerialVersionUID() {
        return serialVersionUID;
    }

    public Double getTraLongitude() {
        return traLongitude;
    }

    public void setTraLongitude(Double traLongitude) {
        this.traLongitude = traLongitude;
    }

    public Double getTraLatitude() {
        return traLatitude;
    }

    public void setTraLatitude(Double traLatitude) {
        this.traLatitude = traLatitude;
    }

    public String getDeviceId() {
        return deviceId;
    }

    public void setDeviceId(String deviceId) {
        this.deviceId = deviceId;
    }
}
