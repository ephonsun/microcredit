package banger.domain.track;

import java.io.Serializable;
import java.util.Date;

import banger.framework.entity.Entity;

/**
 * 地图配置表
 */
public class MapConfig implements Serializable {
	private static final long serialVersionUID = 5224371762647892253L;
	private Integer id;					//主键ID
	private Integer configType;					//配置类型:1,经度配置,2,维度配置,3缩放配置
	private String configValue;					//值
	private Date createDate;					//创建时间
	private Date updateDate;					//更新时间
	private Integer createUser;					//创建用户
	private Integer updateUser;					//更新用户

	public Integer getId(){
		return this.id;
	}

	public void setId(Integer id){
		this.id=id;
	}

	public Integer getConfigType(){
		return this.configType;
	}

	public void setConfigType(Integer configType){
		this.configType=configType;
	}

	public String getConfigValue(){
		return this.configValue;
	}

	public void setConfigValue(String configValue){
		this.configValue=configValue;
	}

	public Date getCreateDate(){
		return this.createDate;
	}

	public void setCreateDate(Date createDate){
		this.createDate=createDate;
	}

	public Date getUpdateDate(){
		return this.updateDate;
	}

	public void setUpdateDate(Date updateDate){
		this.updateDate=updateDate;
	}

	public Integer getCreateUser(){
		return this.createUser;
	}

	public void setCreateUser(Integer createUser){
		this.createUser=createUser;
	}

	public Integer getUpdateUser(){
		return this.updateUser;
	}

	public void setUpdateUser(Integer updateUser){
		this.updateUser=updateUser;
	}

}
