package banger.domain.track;

import java.io.Serializable;
import java.util.Date;

import banger.framework.entity.Entity;

/**
 * 地图经纬度座标轨迹
 */
public class MapTrajectory implements Serializable {
	private static final long serialVersionUID = 5587055423895159332L;
	private Integer traId;					//主键ID
	private Double traLongitude;					//经度
	private Double traLatitude;					//纬度
	private String deviceId;					//设备ID
	private Date clientTime;					//采集时间
	private Date createDate;					//创建时间
	private Integer createUser;					//创建用户

	public Integer getTraId(){
		return this.traId;
	}

	public void setTraId(Integer traId){
		this.traId=traId;
	}

	public Double getTraLongitude(){
		return this.traLongitude;
	}

	public void setTraLongitude(Double traLongitude){
		this.traLongitude=traLongitude;
	}

	public Double getTraLatitude(){
		return this.traLatitude;
	}

	public void setTraLatitude(Double traLatitude){
		this.traLatitude=traLatitude;
	}

	public String getDeviceId(){
		return this.deviceId;
	}

	public void setDeviceId(String deviceId){
		this.deviceId=deviceId;
	}

	public Date getClientTime(){
		return this.clientTime;
	}

	public void setClientTime(Date clientTime){
		this.clientTime=clientTime;
	}

	public Date getCreateDate(){
		return this.createDate;
	}

	public void setCreateDate(Date createDate){
		this.createDate=createDate;
	}

	public Integer getCreateUser(){
		return this.createUser;
	}

	public void setCreateUser(Integer createUser){
		this.createUser=createUser;
	}

}
