package banger.domain.system;

import java.io.Serializable;


public class SysModelConfigExtend extends SysModelConfig{

    private String projectName;

    private String dictCnName;

    public SysModelConfigExtend() {
    }

    public String getProjectName() {
        return projectName;
    }

    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }

    public String getDictCnName() {
        return dictCnName;
    }

    public void setDictCnName(String dictCnName) {
        this.dictCnName = dictCnName;
    }
}
